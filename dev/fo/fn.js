/**
 * Calculates the area of a rectangle.
 * @param {number} w - The width of the rectangle.
 * @param {number} h - The height of the rectangle.
 * @returns {number} The calculated area of the rectangle.
 */
const fn = (w , h) => {
    return w * h
}
export default fn